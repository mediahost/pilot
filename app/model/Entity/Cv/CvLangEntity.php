    public static function scale($id = NULL)
    {
        $scales = array(
            'a1' => "Basic User (A1)",
            'a2' => "Basic User (A2)",
            'b1' => "Independent user (B1)",
            'b2' => "Independent user (B2)",
            'c1' => "Proficient user (C1)",
            'c2' => "Proficient user (C2)",
        );

        if (is_null($id)) {
            return $scales;
        } else {
            if (array_key_exists($id, $scales)) {
                return $scales[$id];
            } else {
                return NULL;
            }
        }
    }

    public static function helperGetScale($scaleId)
    {
        return self::scale($scaleId);
    }

    public static function helperGetScaleHtm1($scaleId)
    {
        $scaleName = self::scale($scaleId);
        if (preg_match("@(.+)\s+\((\w+)\)$@", $scaleName, $matches)) {
            $html = "{$matches[2]}<br />{$matches[1]}";
            return $html;
        } else {
            return $scaleName;
        }
    }

    public static function cmp(CvLangEntity $first, CvLangEntity $second)
    {
        $same = 0;
        $low = -1; // UP; to start of array
        $high = 1; // DOWN; to end of array
        
        $first = self::languages($first->lang);
        $second = self::languages($second->lang);

        if ($first == $second) {
            return $same;
        } else {
            return ($first < $second) ? $low : $high;
        }
    }

